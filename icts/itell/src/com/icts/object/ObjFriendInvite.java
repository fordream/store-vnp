package com.icts.object;

public class ObjFriendInvite {
	private String mInviteId;
	private long mInviteTime;
	private String mFriendID;
	private String mFriendNick;
	private String mFriendAvatar;

	public String getmInviteId() {
		return mInviteId;
	}

	public void setmInviteId(String mInviteId) {
		this.mInviteId = mInviteId;
	}

	public long getmInviteTime() {
		return mInviteTime;
	}

	public void setmInviteTime(long mInviteTime) {
		this.mInviteTime = mInviteTime;
	}

	public String getmFriendID() {
		return mFriendID;
	}

	public void setmFriendID(String mFriendID) {
		this.mFriendID = mFriendID;
	}

	public String getmFriendNick() {
		return mFriendNick;
	}

	public void setmFriendNick(String mFriendNick) {
		this.mFriendNick = mFriendNick;
	}

	public String getmFriendAvatar() {
		return mFriendAvatar;
	}

	public void setmFriendAvatar(String mFriendAvatar) {
		this.mFriendAvatar = mFriendAvatar;
	}

}
