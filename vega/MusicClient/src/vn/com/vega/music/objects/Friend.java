package vn.com.vega.music.objects;

import java.io.Serializable;
import java.util.ArrayList;

@SuppressWarnings("serial")
public class Friend implements Serializable {

	public int friend_id;
	public String friend_name;
	public String friend_info;
	public String friend_avatar;
	public int my_idol;
	public ArrayList<Playlist> friend_playlist;
	public Boolean checked;
	// chacha
	public String avatar;

	public Friend() {
		this.checked = false;
	}

	public Friend(int friend_id, String friend_name, String friend_info,
			String friend_avatar, int friend_follow_me,
			ArrayList<Playlist> friend_playlist) {
		super();
		this.friend_id = friend_id;
		this.friend_name = friend_name;
		this.friend_info = friend_info;
		this.friend_avatar = friend_avatar;
		this.my_idol = friend_follow_me;
		this.friend_playlist = friend_playlist;
		this.checked = false;
	}

	public int getFriend_id() {
		return friend_id;
	}

	public void setFriend_id(int friend_id) {
		this.friend_id = friend_id;
	}

	public String getFriend_name() {
		return friend_name;
	}

	public void setFriend_name(String friend_name) {
		this.friend_name = friend_name;
	}

	public String getFriend_info() {
		return friend_info;
	}

	public void setFriend_info(String friend_info) {
		this.friend_info = friend_info;
	}

	public String getFriend_avatar() {
		return friend_avatar;
	}
	
	public Boolean isChecked() {
		return checked;
	}
	
	public void setChecked(Boolean checked) {
		this.checked = checked;
	}

	public void setFriend_avatar(String friend_avatar) {
		this.friend_avatar = friend_avatar;
	}

	public int getmy_idol() {
		return my_idol;
	}

	public void setmy_idol(int my_idol) {
		this.my_idol = my_idol;
	}

	public ArrayList<Playlist> getFriend_playlist() {
		return friend_playlist;
	}

	public void setFriend_playlist(ArrayList<Playlist> friend_playlist) {
		this.friend_playlist = friend_playlist;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

}
